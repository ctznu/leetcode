package validSudoku;

import static org.junit.Assert.assertEquals;

import org.junit.Test;

public class ValidSudokuTest {
    @Test
    public void testValidSudoku() {
        ValidSudoku vs = new ValidSudoku();
        char[][] board = {
            {'5','3','.','.','7','.','.','.','.'},
            {'6','.','.','1','9','5','.','.','.'},
            {'.','9','8','.','.','.','.','6','.'},
            {'8','.','.','.','6','.','.','.','3'},
            {'4','.','.','8','.','3','.','.','1'},
            {'7','.','.','.','2','.','.','.','6'},
            {'.','6','.','.','.','.','2','8','.'},
            {'.','.','.','4','1','9','.','.','5'},
            {'.','.','.','.','8','.','.','7','9'}
          };
        assertEquals(true, vs.isValidSudoku(board));
        
        char[][] board1 = {
            {'8','3','.','.','7','.','.','.','.'},
            {'6','.','.','1','9','5','.','.','.'},
            {'.','9','8','.','.','.','.','6','.'},
            {'8','.','.','.','6','.','.','.','3'},
            {'4','.','.','8','.','3','.','.','1'},
            {'7','.','.','.','2','.','.','.','6'},
            {'.','6','.','.','.','.','2','8','.'},
            {'.','.','.','4','1','9','.','.','5'},
            {'.','.','.','.','8','.','.','7','9'}
        };
        assertEquals(false, vs.isValidSudoku(board1));
        char[][] board2 = {
            {'.','1','.','5','2','.','.','.','.'},
            {'.','.','.','.','.','6','4','3','.'},
            {'.','.','.','.','.','.','.','.','.'},
            {'5','.','.','.','.','.','9','.','.'},
            {'.','.','.','.','.','.','.','5','.'},
            {'.','.','.','5','.','.','.','.','.'},
            {'9','.','.','.','.','3','.','.','.'},
            {'.','.','6','.','.','.','.','.','.'},
            {'.','.','.','.','.','.','.','.','.'}
        };
        assertEquals(false, vs.isValidSudoku(board2));
    }
}